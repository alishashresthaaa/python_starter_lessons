#Write a program to read three numbers from users and calculate average
# can be float numbers as well
number_one = float(input("Enter the first number: "))
number_two = float(input("Enter the second number: "))
number_three = float(input("Enter the third number: "))

average = (number_one + number_two + number_three) / 3

print("The average of the three numbers is:", average)